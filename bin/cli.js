function writeManagedFile(targetPath, nextContent) {
    if (fs.existsSync(targetPath)) {
        const currentContent = fs.readFileSync(targetPath, "utf8");
        const isManaged = currentContent.includes(GENERATED_BY);

        if (!isManaged) {
            return { written: false };
        }
    }

    fs.mkdirSync(path.dirname(targetPath), { recursive: true });
    fs.writeFileSync(targetPath, nextContent);
    return { written: true };
}

function resolveSourceRoot() {
    const devSourcePath = path.join(__dirname, "..", KIT_DIR);

    if (fs.existsSync(devSourcePath)) {
        return {
            sourceRoot: path.join(__dirname, ".."),
            cleanup: () => {},
        };
    }

    const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), "my-agents-kit-"));
    execSync(`git clone --depth 1 ${REPO_URL} ${tempDir}`, {
        stdio: "ignore",
    });

    return {
        sourceRoot: tempDir,
        cleanup: () => fs.rmSync(tempDir, { recursive: true, force: true }),
    };
}

