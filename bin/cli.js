#!/usr/bin/env node

/**
 * my-agents-kit CLI
 * my-agents-kit
 *
 * Install .agent into your project
 */

const { execSync } = require("child_process");
const fs = require("fs");
const os = require("os");
const path = require("path");

const REPO_URL = "https://github.com/nguyenphu0903/my-agents-kit.git";
const KIT_DIR = ".agent";
const COPILOT_AGENTS_DIR = path.join(".github", "agents");
const COPILOT_SKILLS_DIR = path.join(".github", "skills");
const OPENCODE_AGENTS_DIR = path.join(".opencode", "agents");
const OPENCODE_SKILLS_DIR = path.join(".opencode", "skills");
const INSTALL_PATHS = [KIT_DIR];
const GENERATED_BY = "Generated by my-agents-kit";
const SUPPORTED_PROVIDERS = ["copilot", "opencode", "claude", "gemini"];

function main() {
    const args = process.argv.slice(2);
    const command = args[0] || "init";
    const options = new Set(args.slice(1));

    switch (command) {
        case "init":
            initAgentKit(options);
            break;
        case "update":
            updateAgentKit(options);
            break;
        case "status":
            checkStatus();
            break;
        default:
            console.log(
                "Usage: my-agents-kit [init|update|status] [--global] [--all] [--provider=name]",
            );
            process.exit(1);
    }
}

function resolveSelectedProviders(options) {
    const explicitProviders = parseProviderList(options);

    if (options.has("--all")) {
        return new Set(SUPPORTED_PROVIDERS);
    }

    if (explicitProviders.length > 0) {
        return new Set(
            explicitProviders.filter((provider) =>
                SUPPORTED_PROVIDERS.includes(provider),
            ),
        );
    }

    return new Set([detectPreferredProvider()]);
}

function parseProviderList(options) {
    for (const option of options) {
        for (const prefix of ["--provider=", "--providers=", "--with="]) {
            if (option.startsWith(prefix)) {
                return option
                    .slice(prefix.length)
                    .split(",")
                    .map((value) => value.trim())
                    .filter(Boolean);
            }
        }
    }

    return [];
}

function detectPreferredProvider() {
    if (isCopilotEnvironment()) {
        return "copilot";
    }

    if (
        isCommandAvailable("opencode") ||
        fs.existsSync(path.join(os.homedir(), ".config", "opencode"))
    ) {
        return "opencode";
    }

    if (
        isCommandAvailable("claude") ||
        fs.existsSync(path.join(os.homedir(), ".claude"))
    ) {
        return "claude";
    }

    if (
        isCommandAvailable("gemini") ||
        fs.existsSync(path.join(os.homedir(), ".gemini"))
    ) {
        return "gemini";
    }

    return "copilot";
}

function isCopilotEnvironment() {
    return Boolean(
        process.env.VSCODE_PID ||
        process.env.VSCODE_USER_PROMPTS_FOLDER ||
        process.env.TERM_PROGRAM === "vscode" ||
        isCommandAvailable("code"),
    );
}

function isCommandAvailable(commandName) {
    try {
        execSync(`command -v ${commandName}`, { stdio: "ignore" });
        return true;
    } catch {
        return false;
    }
}

function initAgentKit(options) {
    if (options.has("--global")) {
        initGlobalAgentKit(options);
        return;
    }

    console.log("🚀 Installing my-agents-kit...");

    const targetDir = process.cwd();
    const kitPath = path.join(targetDir, KIT_DIR);
    const selectedProviders = resolveSelectedProviders(options);

    if (fs.existsSync(kitPath)) {
        console.log("⚠️  .agent folder already exists!");
        console.log('Use --force to overwrite or run "my-agents-kit update"');
        process.exit(1);
    }

    try {
        // Check if running in development mode (local testing)
        const devSourcePath = path.join(__dirname, "..", KIT_DIR);

        if (fs.existsSync(devSourcePath)) {
            // Development mode: copy from local
            console.log("📦 Using local development version...");
            installAssets(path.join(__dirname, ".."), targetDir);
            publishWorkspaceProviderAssets(
                path.join(__dirname, ".."),
                targetDir,
                selectedProviders,
            );
            console.log("✅ my-agents-kit installed successfully!");
        } else {
            // Production mode: clone from GitHub
            const tempDir = path.join(targetDir, ".my-agents-kit-temp");
            console.log("📦 Downloading latest version...");
            execSync(`git clone --depth 1 ${REPO_URL} ${tempDir}`, {
                stdio: "ignore",
            });

            if (fs.existsSync(path.join(tempDir, KIT_DIR))) {
                installAssets(tempDir, targetDir);
                publishWorkspaceProviderAssets(
                    tempDir,
                    targetDir,
                    selectedProviders,
                );
                console.log("✅ my-agents-kit installed successfully!");
            } else {
                throw new Error(".agent folder not found in repository");
            }

            // Cleanup
            fs.rmSync(tempDir, { recursive: true, force: true });
        }

        // Auto-exclude .agent from git (if git repo exists)
        const gitExcludePath = path.join(targetDir, ".git", "info", "exclude");
        if (fs.existsSync(path.join(targetDir, ".git"))) {
            try {
                let excludeContent = "";
                if (fs.existsSync(gitExcludePath)) {
                    excludeContent = fs.readFileSync(gitExcludePath, "utf8");
                }

                // Add .agent to exclude if not already there
                if (!excludeContent.includes(".agent")) {
                    excludeContent += "\n# my-agents-kit\n.agent/\n";
                    fs.writeFileSync(gitExcludePath, excludeContent);
                    console.log("✅ Added .agent to git exclude (local only)");
                }

                if (
                    selectedProviders.has("copilot") &&
                    !excludeContent.includes(".github/agents")
                ) {
                    excludeContent += ".github/agents/\n";
                    fs.writeFileSync(gitExcludePath, excludeContent);
                    console.log(
                        "✅ Added .github/agents to git exclude (local only)",
                    );
                }

                const localOnlyArtifacts = [
                    selectedProviders.has("opencode") ? ".opencode/" : null,
                    selectedProviders.has("opencode") ? "AGENTS.md" : null,
                    selectedProviders.has("claude") ? "CLAUDE.md" : null,
                    selectedProviders.has("gemini") ? "GEMINI.md" : null,
                    selectedProviders.has("copilot")
                        ? ".github/copilot-instructions.md"
                        : null,
                ].filter(Boolean);
                for (const artifactPath of localOnlyArtifacts) {
                    if (!excludeContent.includes(artifactPath)) {
                        excludeContent += `${artifactPath}\n`;
                    }
                }
                fs.writeFileSync(gitExcludePath, excludeContent);
            } catch (err) {
                // Silently fail if can't write to git exclude
                console.log(
                    '⚠️  Could not auto-exclude from git (run manually: add ".agent/", ".github/agents/", ".opencode/", "AGENTS.md", "CLAUDE.md", "GEMINI.md", and ".github/copilot-instructions.md" to .git/info/exclude)',
                );
            }
        }

        console.log("");
        console.log("🎯 my-agents-kit is ready!");
        console.log("");
        console.log("📚 Available agents:");
        console.log("  - golang-specialist (Go, Kafka, NATS, Redis)");
        console.log("  - backend-specialist (Go, Node.js, Python, API)");
        console.log("  - frontend-specialist (React, Next.js, UI/UX)");
        console.log("  - mobile-developer (React Native, Flutter)");
        console.log("  - And 11 more...");
        console.log("");
        console.log('💡 Try: "Use golang-specialist to review this Go code"');
    } catch (error) {
        console.error("❌ Installation failed:", error.message);
        process.exit(1);
    }
}

function updateAgentKit(options) {
    if (options.has("--global")) {
        updateGlobalAgentKit(options);
        return;
    }

    console.log("🔄 Updating my-agents-kit...");

    const targetDir = process.cwd();
    const kitPath = path.join(targetDir, KIT_DIR);
    const selectedProviders = resolveSelectedProviders(options);

    if (!fs.existsSync(kitPath)) {
        console.log(
            '⚠️  .agent folder not found. Run "my-agents-kit init" first.',
        );
        process.exit(1);
    }

    try {
        const tempDir = path.join(targetDir, ".my-agents-kit-temp");
        console.log("📦 Downloading latest version...");
        execSync(`git clone --depth 1 ${REPO_URL} ${tempDir}`, {
            stdio: "ignore",
        });

        if (fs.existsSync(path.join(tempDir, KIT_DIR))) {
            fs.rmSync(kitPath, { recursive: true, force: true });
            installAssets(tempDir, targetDir);
            publishWorkspaceProviderAssets(
                tempDir,
                targetDir,
                selectedProviders,
            );
            console.log("✅ Updated successfully!");
        } else {
            throw new Error(".agent folder not found in repository");
        }

        fs.rmSync(tempDir, { recursive: true, force: true });
    } catch (error) {
        console.error("❌ Update failed:", error.message);
        process.exit(1);
    }
}

function installAssets(sourceRoot, targetDir) {
    for (const relativePath of INSTALL_PATHS) {
        const sourcePath = path.join(sourceRoot, relativePath);
        const targetPath = path.join(targetDir, relativePath);

        if (!fs.existsSync(sourcePath)) {
            continue;
        }

        fs.cpSync(sourcePath, targetPath, { recursive: true });
    }
}

function generateCopilotAgents(sourceRoot, targetDir, useGithubPrefix = true) {
    const sourceAgentsDir = path.join(sourceRoot, KIT_DIR, "agents");

    if (!fs.existsSync(sourceAgentsDir)) {
        return;
    }

    const targetAgentsDir = useGithubPrefix
        ? path.join(targetDir, COPILOT_AGENTS_DIR)
        : path.join(targetDir, "agents");
    fs.mkdirSync(targetAgentsDir, { recursive: true });

    const agentFiles = fs
        .readdirSync(sourceAgentsDir)
        .filter((file) => file.endsWith(".md"));

    for (const fileName of agentFiles) {
        const sourcePath = path.join(sourceAgentsDir, fileName);
        const targetPath = path.join(
            targetAgentsDir,
            fileName.replace(/\.md$/, ".agent.md"),
        );
        const sourceContent = fs.readFileSync(sourcePath, "utf8");
        const agentDoc = toCopilotAgentMarkdown(sourceContent, fileName);
        fs.writeFileSync(targetPath, agentDoc);
    }

    console.log(
        `✅ Generated ${agentFiles.length} GitHub Copilot custom agents in ${targetAgentsDir}`,
    );
}

function generateCopilotSkills(sourceRoot, targetDir, useGithubPrefix = true) {
    const sourceSkillsDir = path.join(sourceRoot, KIT_DIR, "skills");

    if (!fs.existsSync(sourceSkillsDir)) {
        return;
    }

    const skillDirs = fs
        .readdirSync(sourceSkillsDir)
        .filter((entry) =>
            fs.statSync(path.join(sourceSkillsDir, entry)).isDirectory(),
        );

    let exportedCount = 0;
    for (const skillName of skillDirs) {
        const sourceSkillPath = path.join(
            sourceSkillsDir,
            skillName,
            "SKILL.md",
        );

        if (!fs.existsSync(sourceSkillPath)) {
            continue;
        }

        const targetSkillPath = path.join(
            targetDir,
            useGithubPrefix ? COPILOT_SKILLS_DIR : "skills",
            skillName,
            "SKILL.md",
        );

        const sourceContent = fs.readFileSync(sourceSkillPath, "utf8");
        const managedContent = toManagedMarkdown(
            sourceContent,
            path.join(KIT_DIR, "skills", skillName, "SKILL.md"),
        );
        const writeResult = writeManagedFile(targetSkillPath, managedContent);

        if (writeResult.written) {
            exportedCount += 1;
        }
    }

    console.log(
        `✅ Published ${exportedCount}/${skillDirs.length} Copilot skills in ${useGithubPrefix ? COPILOT_SKILLS_DIR : "skills"}`,
    );
}

function generateOpenCodeAssets(sourceRoot, targetDir, useDotPrefix = true) {
    generateOpenCodeAgents(sourceRoot, targetDir, useDotPrefix);
    generateOpenCodeSkills(sourceRoot, targetDir, useDotPrefix);
}

function generateOpenCodeAgents(sourceRoot, targetDir, useDotPrefix = true) {
    const sourceAgentsDir = path.join(sourceRoot, KIT_DIR, "agents");

    if (!fs.existsSync(sourceAgentsDir)) {
        return;
    }

    const targetAgentsDir = useDotPrefix
        ? path.join(targetDir, OPENCODE_AGENTS_DIR)
        : path.join(targetDir, "agents");
    fs.mkdirSync(targetAgentsDir, { recursive: true });

    const agentFiles = fs
        .readdirSync(sourceAgentsDir)
        .filter((file) => file.endsWith(".md"));

    let writtenCount = 0;

    for (const fileName of agentFiles) {
        const sourcePath = path.join(sourceAgentsDir, fileName);
        const targetPath = path.join(targetAgentsDir, fileName);
        const sourceContent = fs.readFileSync(sourcePath, "utf8");
        const agentDoc = toOpenCodeAgentMarkdown(sourceContent, fileName);
        const writeResult = writeManagedFile(targetPath, agentDoc);

        if (writeResult.written) {
            writtenCount += 1;
        } else {
            console.log(
                `⚠️  Keeping existing ${path.join(OPENCODE_AGENTS_DIR, fileName)} (user-managed)`,
            );
        }
    }

    console.log(
        `✅ Published ${writtenCount}/${agentFiles.length} OpenCode agents in ${OPENCODE_AGENTS_DIR}`,
    );
}

function generateOpenCodeSkills(sourceRoot, targetDir, useDotPrefix = true) {
    const sourceSkillsDir = path.join(sourceRoot, KIT_DIR, "skills");

    if (!fs.existsSync(sourceSkillsDir)) {
        return;
    }

    const skillDirs = fs
        .readdirSync(sourceSkillsDir)
        .filter((entry) =>
            fs.statSync(path.join(sourceSkillsDir, entry)).isDirectory(),
        );

    let exportedCount = 0;
    for (const skillName of skillDirs) {
        if (!isOpenCodeSkillName(skillName)) {
            console.log(
                `⚠️  Skipping skill ${skillName}: invalid OpenCode skill name format`,
            );
            continue;
        }

        const sourceSkillPath = path.join(
            sourceSkillsDir,
            skillName,
            "SKILL.md",
        );
        if (!fs.existsSync(sourceSkillPath)) {
            continue;
        }

        const targetSkillPath = path.join(
            targetDir,
            useDotPrefix ? OPENCODE_SKILLS_DIR : "skills",
            skillName,
            "SKILL.md",
        );

        const sourceContent = fs.readFileSync(sourceSkillPath, "utf8");
        const skillDoc = toOpenCodeSkillMarkdown(sourceContent, skillName);
        const writeResult = writeManagedFile(targetSkillPath, skillDoc);

        if (writeResult.written) {
            exportedCount += 1;
        } else {
            console.log(
                `⚠️  Keeping existing ${path.join(OPENCODE_SKILLS_DIR, skillName, "SKILL.md")} (user-managed)`,
            );
        }
    }

    console.log(
        `✅ Published ${exportedCount}/${skillDirs.length} OpenCode skills in ${OPENCODE_SKILLS_DIR}`,
    );
}

function publishWorkspaceProviderAssets(sourceRoot, targetDir, providers) {
    if (providers.has("copilot")) {
        publishCopilotWorkspaceAssets(sourceRoot, targetDir);
    }

    if (providers.has("opencode")) {
        publishOpenCodeWorkspaceAssets(sourceRoot, targetDir);
    }

    if (providers.has("claude")) {
        publishClaudeWorkspaceAssets(sourceRoot, targetDir);
    }

    if (providers.has("gemini")) {
        publishGeminiWorkspaceAssets(sourceRoot, targetDir);
    }
}

function publishGlobalProviderAssets(sourceRoot, providers) {
    if (providers.has("copilot")) {
        publishCopilotGlobalAssets(sourceRoot);
    }

    if (providers.has("opencode")) {
        publishOpenCodeGlobalAssets(sourceRoot);
    }

    if (providers.has("claude")) {
        publishClaudeGlobalAssets(sourceRoot);
    }

    if (providers.has("gemini")) {
        publishGeminiGlobalAssets(sourceRoot);
    }
}

function publishCopilotWorkspaceAssets(sourceRoot, targetDir) {
    generateCopilotAgents(sourceRoot, targetDir, true);
    generateCopilotSkills(sourceRoot, targetDir, true);
    publishManagedMarkdownFile(
        sourceRoot,
        path.join(".github", "copilot-instructions.md"),
        targetDir,
        path.join(".github", "copilot-instructions.md"),
    );
}

function publishCopilotGlobalAssets(sourceRoot) {
    const agentsRoot = getGlobalCopilotAgentsRoot();
    const skillsRoot = getGlobalCopilotSkillsRoot();
    const instructionsRoot = getGlobalCopilotInstructionsRoot();

    fs.mkdirSync(agentsRoot, { recursive: true });
    fs.mkdirSync(skillsRoot, { recursive: true });
    fs.mkdirSync(instructionsRoot, { recursive: true });

    const sourceAgentsDir = path.join(sourceRoot, KIT_DIR, "agents");
    const agentFiles = fs
        .readdirSync(sourceAgentsDir)
        .filter((file) => file.endsWith(".md"));

    for (const fileName of agentFiles) {
        const sourcePath = path.join(sourceAgentsDir, fileName);
        const targetPath = path.join(
            agentsRoot,
            fileName.replace(/\.md$/, ".agent.md"),
        );
        const sourceContent = fs.readFileSync(sourcePath, "utf8");
        const agentDoc = toCopilotAgentMarkdown(sourceContent, fileName);
        writeManagedFile(targetPath, agentDoc);
    }

    generateCopilotSkills(
        sourceRoot,
        path.join(os.homedir(), ".copilot"),
        false,
    );

    const instructionSource = path.join(
        sourceRoot,
        ".github",
        "copilot-instructions.md",
    );
    if (fs.existsSync(instructionSource)) {
        const instructionContent = fs.readFileSync(instructionSource, "utf8");
        const instructionDoc = toGlobalCopilotInstructionsMarkdown(
            instructionContent,
            path.join(".github", "copilot-instructions.md"),
        );
        writeManagedFile(
            path.join(instructionsRoot, "my-agents-kit.instructions.md"),
            instructionDoc,
        );
    }

    console.log(
        `✅ Published global Copilot assets in ${agentsRoot}, ${skillsRoot}, and ${instructionsRoot}`,
    );
}

